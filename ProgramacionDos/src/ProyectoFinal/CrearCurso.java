    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        while(true){
        String codigoCurso = ""+Curso.listaDeCursos.size();
        String nombre = jTextField1.getText();
        String descripcion = jTextArea1.getText();
        if(BaseDatosSimulada.cursos.contains(nombre)){
            JOptionPane.showMessageDialog(null,"El curso ya existe");break;
        }
        if(nombre.equals("")){JOptionPane.showMessageDialog(null,"Por favor escoja un nombre");break;}
        if(!nombre.equals("")){
            Curso nuevoCurso = new Curso(nombre, codigoCurso, descripcion,"");
            BaseDatosSimulada.cursos.add(nombre);
            Curso.listaDeCursos.add(nuevoCurso);
            jTextField2.setText("0000"+Curso.listaDeCursos.size());
            JOptionPane.showMessageDialog(null, "Curso creado exitosamente");
            break;
        }
        break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed


